			<h2>Une nouvelle hausse de 5,2% vient d'entrer en vigueur</h2>
			<p>Une nouvelle qui a suscité une vague de contestation auprès des Français. Au point que le gouvernement envisage un gel du prix du gaz jusqu'à la présidentielle.
			Vendredi après-midi une réunion interministérielle s'est tenue à Matignon. 
			Elle rassemblait le Premier ministre François Fillon, la ministre de l'Économie 
			Christine Lagarde, son homologue de l'Énergie Éric Besson et le secrétaire d'État 
			à la Consommation, Frédéric Lefebvre.

			<p>Au menu : la question des prix de l'énergie et notamment celle de la formule 
			permettant de fixer les prix du gaz, rapporte Le Parisien. Toutefois Matignon 
			a assuré que cette réunion ne déboucherait sur aucune décision.</p>

			<p>Mais le journal Les Échos croit savoir que le gouvernement envisagerait de geler
			l'évolution des tarifs jusqu'à la présidentielle de 2012.</p>

			<p>Car les prix du gaz doivent être revus en juillet prochain et devraient augmenter 
			dans l'hypothèse d'une hausse des cours du pétrole. Le gel des prix du gaz devrait 
			donc intervenir en juillet.</p>

			<p>"La question d'un gel se pose pour le 1er juillet, mais le vrai sujet est celui 
			du mode de calcul de ces tarifs", précise un proche du dossier. Cette nouvelle 
			formule prendrait mieux en compte les prix du marché (prix spot) au détriment 
			des prix à long terme, moins avantageux, indique Ouest-France.</p>

			<p>La hausse des prix du gaz s'inscrit dans un contexte de renchérissement des 
			prix de l'énergie (+ 15 % en un an) des prix du pétrole brut.</p>
