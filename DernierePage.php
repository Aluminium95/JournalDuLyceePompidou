				Le projet de la région « école numérique » se prépare, et pour cause, l’année prochaine, les secondes auront chacun la chance de recevoir un ordinateur portable, un don de la région. Il sera doté d’un écran 13", d’une bonne autonomie, du logiciel Windows et de la suite bureautique OpenOffice pour un poids (plume ?) : inférieur à 2,3kg.

Du côté du lycée, le service informatique a déjà installé des bornes Wifi aux quatre coins de l’établissement : au CDI, à la cafeteria, dans la cour… De plus, afin de faciliter la communication entre lycéens et professeurs, le rectorat va mettre en place un environnement de travail numérique, où chacun pourra consulter son agenda, ses cours ou autre, et où les profs pourrons publier des cours.

Bien sûr, dites-vous. Mais quelle est l’utilité d’un tel investissement au lycée ? C’est justement la question que l’on a posé à de multiples personnes.

L’avis des enseignants est plutôt positif, mais tout de même mitigé. Un professeur de mathématiques pense que cela lui permettra de faire des « travaux pratiques », avec l’ordinateur, dans la dynamique de son cours : il ne sera plus obligé d’attendre qu’il ait une salle informatique pour consacrer une heure entière à un TP. Il déplore par contre l’absence des manuels numérique et se demande ce qu’il fera si un élève casse son ordinateur, par exemple. Un autre professeur revient sur ces problèmes, qui sont pour lui analogues à ceux qu’il rencontre habituellement tel que la perte malencontreuse d’un cahier… Puis, un dernier professeur approuve l’usage de l’outil informatique pendant ses cours, ce qui lui permettra de faire des simulations, entrer autre.

Les élève pensent globalement que d’équiper les secondes d’ordinateurs est une bonne initiative, souffrant de quelques défauts. Prenons le poids de l’ordinateur + l’absence de manuel numérique. Après un moment de réflexion on s’aperçoit que… le poids du sac d’un lycéen va augmenter ! Heureusement, cette situation va évoluer avec le temps.

Et les documentalistes du lycée pensent que l’outil informatique est très intéressant pédagogiquement, et que c’est important que cet outil soit donné, pour l’égalité des chances. Elles  remarquent qu’il faut que (certains ?) lycéens arrêtent de voir un ordinateur uniquement comme un jouet, mais aussi comme un outil pour leurs études.

Cette nouveauté est donc globalement perçue comme étant une bonne initiative, mais ayant malheureusement des défauts non négligeable, qui, espérons-le, se résoudront avec le temps. Il est normal de rencontrer des problèmes au lancement qui n’est en somme qu’un premier pas.
